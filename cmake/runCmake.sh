OS=`uname`

#-------------------------------------------------------------------------------
# Apple macOS (Darwin)
#-------------------------------------------------------------------------------

[ "$OS" == 'Darwin' ] && {
  cmake .. -G "Unix Makefiles"
# cmake .. -G "Xcode"
  exit 0
}

#-------------------------------------------------------------------------------
# Linux
#-------------------------------------------------------------------------------

[ "$OS" == 'Linux' ] && {
# cmake .. -G "CodeLite - Ninja"
  cmake .. -G "Unix Makefiles"
  exit 0
}

#-------------------------------------------------------------------------------
# Microsoft Windows and Visual Studio 2019.
#-------------------------------------------------------------------------------

cmake .. -G "Visual Studio 16 2019" -Wno-dev
